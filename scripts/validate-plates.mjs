const prefectures = Object.keys(origins).filter(
  (c) => c.endsWith('00') && !c.endsWith('0000'),
)

const mismatches = []
for (const code of prefectures) {
  const expected = IDCD_REF[code]
  if (!expected) continue
  const actual = PLATE_BY_ADCODE[code]
  if (actual !== expected) {
    mismatches.push({ code, name: origins[code].slice(0, 8), actual, expected })
  }
}

if (mismatches.length === 0) {
  console.log(`OK: ${prefectures.length} 个地级区划与参考表一致（已对照 ${Object.keys(IDCD_REF).length} 条）`)
} else {
  console.log(`发现 ${mismatches.length} 处错误：`)
  for (const m of mismatches) {
    console.log(`  ${m.code} ${m.name}…  当前 ${m.actual}  → 应为 ${m.expected}`)
  }
  process.exit(1)
}
